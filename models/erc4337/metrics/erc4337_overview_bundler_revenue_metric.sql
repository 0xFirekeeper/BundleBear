WITH day_metrics AS (
SELECT 
TO_VARCHAR(DATE, 'YYYY-MM-DD') as DATE,
'day' AS TIMEFRAME,
chain,
SUM(REVENUE) AS REVENUE
FROM BUNDLEBEAR.DBT_KOFI.ERC4337_ALL_DAY_BUNDLER_REVENUE_CHAIN
WHERE DATE > DATE_TRUNC('month', CURRENT_DATE()) - INTERVAL '24 months'
GROUP BY 1,2,3
)

, week_metrics AS (
SELECT 
TO_VARCHAR(date_trunc('week', DATE), 'YYYY-MM-DD') as DATE,
'week' AS TIMEFRAME,
chain,
SUM(REVENUE) AS REVENUE
FROM BUNDLEBEAR.DBT_KOFI.ERC4337_ALL_DAY_BUNDLER_REVENUE_CHAIN
WHERE DATE > DATE_TRUNC('month', CURRENT_DATE()) - INTERVAL '24 months'
GROUP BY 1,2,3
)

, month_metrics AS (
SELECT 
TO_VARCHAR(date_trunc('month', DATE), 'YYYY-MM-DD') as DATE,
'month' AS TIMEFRAME,
chain,
SUM(REVENUE) AS REVENUE
FROM BUNDLEBEAR.DBT_KOFI.ERC4337_ALL_DAY_BUNDLER_REVENUE_CHAIN
WHERE DATE > DATE_TRUNC('month', CURRENT_DATE()) - INTERVAL '24 months'
GROUP BY 1,2,3
)

SELECT
DATE,
TIMEFRAME,
CHAIN,
REVENUE
FROM day_metrics

UNION ALL
SELECT
DATE,
TIMEFRAME,
CHAIN,
REVENUE
FROM week_metrics

UNION ALL
SELECT
DATE,
TIMEFRAME,
CHAIN,
REVENUE
FROM month_metrics